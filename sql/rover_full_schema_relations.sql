CREATE TABLE bookmarks (
    bookmark_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    url         VARCHAR2(2000) NOT NULL,
    title       VARCHAR2(500)  NOT NULL,
    category    VARCHAR2(100),
    starred     NUMBER(1) DEFAULT 0 NOT NULL,
    notes       CLOB,
    created_at  TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at  TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_bookmarks_url UNIQUE (url),
    CONSTRAINT chk_bookmarks_starred CHECK (starred IN (0, 1))
);


CREATE TABLE bookmark_study_cards (
    card_id         NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    bookmark_id     NUMBER NOT NULL,
    card_no         NUMBER NOT NULL,
    question        VARCHAR2(500) NOT NULL,
    answer          CLOB NOT NULL,
    source_excerpt  CLOB,
    created_at      TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at      TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_bsc_bookmark FOREIGN KEY (bookmark_id)
        REFERENCES bookmarks (bookmark_id)
        ON DELETE CASCADE,
    CONSTRAINT chk_bsc_card_no CHECK (card_no >= 1)
);

CREATE INDEX idx_bsc_bookmark_no ON bookmark_study_cards (bookmark_id, card_no);


CREATE TABLE bookmark_study_card_jobs (
    job_id         NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    bookmark_id    NUMBER NOT NULL,
    status         VARCHAR2(20) NOT NULL,
    error_message  VARCHAR2(2000),
    created_at     TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    started_at     TIMESTAMP,
    finished_at    TIMESTAMP,
    updated_at     TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_bscj_bookmark FOREIGN KEY (bookmark_id)
        REFERENCES bookmarks (bookmark_id)
        ON DELETE CASCADE,
    CONSTRAINT chk_bscj_status CHECK (status IN ('QUEUED', 'RUNNING', 'COMPLETED', 'FAILED'))
);

CREATE INDEX idx_bscj_bookmark_status ON bookmark_study_card_jobs (bookmark_id, status, created_at);


