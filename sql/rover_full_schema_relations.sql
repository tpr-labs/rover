-- 5) MESSENGER
--------------------------------------------------------------------------------

CREATE TABLE messenger_messages (
    message_id             NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    message_text           CLOB NOT NULL,
    status                 VARCHAR2(20) NOT NULL,
    telegram_message_id    VARCHAR2(100),
    telegram_chat_id       VARCHAR2(120),
    http_status            NUMBER,
    response_payload       CLOB,
    error_message          VARCHAR2(4000),
    is_hidden              CHAR(1) DEFAULT 'N' NOT NULL,
    resend_of_message_id   NUMBER,
    created_at             TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at             TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT chk_messenger_messages_status CHECK (status IN ('SENT', 'FAILED')),
    CONSTRAINT chk_messenger_messages_hidden CHECK (is_hidden IN ('Y', 'N')),
    CONSTRAINT fk_messenger_messages_resend_of FOREIGN KEY (resend_of_message_id)
        REFERENCES messenger_messages(message_id)
        ON DELETE SET NULL
);

CREATE INDEX idx_messenger_messages_status ON messenger_messages(status);
CREATE INDEX idx_messenger_messages_hidden ON messenger_messages(is_hidden);
CREATE INDEX idx_messenger_messages_created ON messenger_messages(created_at);
CREATE INDEX idx_messenger_messages_resend ON messenger_messages(resend_of_message_id);


