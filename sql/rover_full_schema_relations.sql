-- 6) RELATIONSHIP SUMMARY (ER QUICK MAP)
--------------------------------------------------------------------------------
-- sb_folders.parent_folder_id                 -> sb_folders.folder_id        (self tree)
-- sb_files.folder_id                          -> sb_folders.folder_id         (folder contains files)
-- sb_file_links.file_id_low                   -> sb_files.file_id             (file-to-file link)
-- sb_file_links.file_id_high                  -> sb_files.file_id             (file-to-file link)
-- uploads_sb_file_links.upload_id             -> uploads_files.upload_id      (M:N uploads<->sb_files)
-- uploads_sb_file_links.file_id               -> sb_files.file_id             (M:N uploads<->sb_files)
-- ft_transactions.account_id                  -> ft_accounts.account_id       (tx belongs to account)
-- ft_llm_calls.transaction_id                 -> ft_transactions.transaction_id (LLM audit for tx)
-- uploads_ft_transaction_links.transaction_id -> ft_transactions.transaction_id (M:N uploads<->ft_transactions)
-- uploads_ft_transaction_links.upload_id      -> uploads_files.upload_id      (M:N uploads<->ft_transactions)
-- bookmark_study_cards.bookmark_id            -> bookmarks.bookmark_id        (1:N cards per bookmark)
-- bookmark_study_card_jobs.bookmark_id        -> bookmarks.bookmark_id        (1:N jobs per bookmark)
-- messenger_messages.resend_of_message_id     -> messenger_messages.message_id (self resend chain)
